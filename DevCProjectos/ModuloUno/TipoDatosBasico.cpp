#include <iostream>
using namespace std;

int main (){
	//Tipo de dato entero
	int entero = 15;
	
	//Tipo de dato flotante, uso de punto para decimales
	float flotante = 10.56;
	
	//Tipo de dato double
	double mayor=16.3556;
	
	//tipo de dato char
	char letra ='a';
	
	cout <<"El numero entero es: " << entero << endl;
	cout <<"El numero flotante es: " << flotante << endl;
	cout <<"El numero double es: "<< mayor << endl;
	cout <<"La letra es: "<< letra << endl;

	return 0;
}
